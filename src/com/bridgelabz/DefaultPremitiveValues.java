package com.bridgelabz;

public class DefaultPremitiveValues {
    static int i;
    static char ch;
    static double d;
    static float fl;
    static long lon;
    static short sh;
    static boolean b;
    static byte byt;

    public static void main(String[] args) {

        System.out.println("PREMITIVES DATA TYPES AND IT'S DEFAULT VALUES. ");
        System.out.println("Default value of " + "int = " +i);
        System.out.println("Default value of " + "char = " +ch);
        System.out.println("Default value of " + "double = " +d);
        System.out.println("Default value of " + "float = " +fl);
        System.out.println("Default value of " + "long = " +lon);
        System.out.println("Default value of " + "short = " +sh);
        System.out.println("Default value of " + "boolean = " +b);
        System.out.println("Default value of " + "byte = " +byt);


    }
}
